#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	char		*d;
	char		*s;

	d = (char *) dst;
	s = (char *) src;
	if (!dst && !src)
		return (dst);
	if (s < d)
	{
		while (n--)
			d[n] = s[n];
	}
	else
	{
		while (n--)
			*d++ = *s++;
	}
	return (dst);
}
